// push pop shift unshift


// push
// push uses to add element into array at last position

let array = ["apple","mango","banana","grapes","PineApple"]

console.log(array)
array.push("orange")
console.log(array)


// pop 

// pop use to remove from last position

let deletedItme = array.pop()
console.log(array)
console.log("deleted item from array:- ", deletedItme)


// unshift
// unshift use to add element into array at first position

array.unshift("Harsh")
console.log(array)


// shift
// shift use to remove from first position

let removedItem =  array.shift()
console.log(array)
console.log(removedItem)


